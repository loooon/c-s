public enum DemandTypeEnum
{
    DUN("催收", "icon6.png", "dun"), CUSTOMER_SERVICE("客服", "icon5.png", "customer_service"), GENERAL("普通", "icon7.png",
            "general"), HIGH_RISK("高风险", "icon8.png", "high_risk");
    private String text;

    private String imgPath;

    private String className;

    DemandTypeEnum(String text, String imgPath, String className)
    {
        this.text = text;
        this.imgPath = imgPath;
        this.className = className;
    }

    public String getText()
    {
        return text;
    }

    public String getImgPath()
    {
        return imgPath;
    }

    public String getClassName()
    {
        return className;
    }
}
